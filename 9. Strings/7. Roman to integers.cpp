// all roman integers will be added only
// we will only subtract when the element at index i is lesser than index i+1  (we are travelling from bak to front)
// this is becaue right to left roma nunerals increase hone chahiye, byt uyahan kamm ho rahe
// to this means this is one of those 6 cases jahan par subtract karte hain

int romanToInt(string s) 
{
    unordered_map<char, int> T = { { 'I' , 1 },
                                   { 'V' , 5 },
                                   { 'X' , 10 },
                                   { 'L' , 50 },
                                   { 'C' , 100 },
                                   { 'D' , 500 },
                                   { 'M' , 1000 } };
                                   
   int sum = T[s.back()];
   for (int i = s.length() - 2; i >= 0; --i) 
   {
       if (T[s[i]] < T[s[i + 1]])
       {
           sum -= T[s[i]];
       }
       else
       {
           sum += T[s[i]];
       }
   }
   
   return sum;
}